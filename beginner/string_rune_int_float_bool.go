package main

import "fmt"

// main function where exection starts
func main(){
    
    // string in double quote
    fmt.Println("This is normal string which can have 123, #$%^&, true and false, 34.5")
    
    // char or rune in single quote gives value
    fmt.Println('K') // gives 75
    
    // bool value
    fmt.Println(true)
    fmt.Println(false)
    
    // num and float
    fmt.Println(34)
    fmt.Println(116.5)
}
